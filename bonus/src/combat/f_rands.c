#include "../../include/cub3d.h"

uint32_t	f_randi(uint32_t idx)
{
	idx = (idx << 13) ^ idx;
	return ((idx * (idx * idx * 15731 + 789221) + 1376312589) & 0x7fffffff);
}

uint32_t	f_rand(void)
{
	return (rand() % 100);
}
